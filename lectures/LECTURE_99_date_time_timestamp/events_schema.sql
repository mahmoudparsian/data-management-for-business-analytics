-- MySQL Script: events table with DATE, TIME, TIMESTAMP

-- Drop table if it exists
DROP TABLE IF EXISTS events;

-- Create table
CREATE TABLE events (
    event_id INT PRIMARY KEY,
    event_name VARCHAR(50),
    event_date DATE,
    event_time TIME,
    event_timestamp TIMESTAMP
);

